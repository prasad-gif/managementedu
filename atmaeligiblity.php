<h1>ELIGIBLITY CRITERIA FOR ATMA EXAMINATION</h1>
<p>To fulfill entrance eligiblity criteria is important. Before filling the entrance application form candidates need to check the eligiblity criteria and then proceed further. Below are the criteria that is been provided by AIIMS</p>
<table>
  <tr>
    <th>FACTORS</th>
    <th>DETAILS</th>
</tr>
<tr>
  <td>Examination held by</td>
  <td>AIIMS</td>
</tr>
<tr>
  <td>Mode of exam</td>
  <td>Online mode</td>
</tr>
<tr>
  <td>Age limit</td>
  <td>No age barriers for ATMA. candidates that have applied earlier can also apply.</td>
</tr>
<tr>
  <td>Nationality</td>
  <td>Candidate must be an Indian</td>
</tr>
<tr>
  <td>Educational qualification</td>
  <td><ul>
    <li>Candidate having graduation degree from a recognized university can apply</li>
    <li>Final year candidates can also apply unless they pass in their last year</li>
    <li>Candidates must secure minimum qualifying marks as provided by AIIMS that is 50% for general category and 45% for sc/nt/pwd</li>
  </ul></td>
</tr>
<tr>
  <td>Work experience</td>
  <td>AIIMS do not need candidates to have a mandatory work experience. candidates with no work experience can also apply.</td>
</tr>
<tr>
  <td>Number of attempts</td>
  <td>Like other entrance exams ATMA is not just held once in a year. ATMA is held 4-5 times in a year. candidates can take advantage of this and score as high as they can.</td>
</tr>
</table>


