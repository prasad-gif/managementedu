<h1>ATMA EXAM PATTERN</h1>
<p>ATMA exam pattern is totally different from other entrance examinations. Altough it is easy it is lengthy. ATMA has 180 questions and 180 minutes. research says you need to attend 100 questions out of total 180 questions with 95% accuracy. But the truth is you need to attend almost 130-140 questions with 95% accuracy. This varies according to the candidates accuracy in attending questions. The low your accuracy the higher questions you need to attend. some candidates even attend 170+ questions. So it is suggested to the candidates to not be reliable on accuracy and questions attended and attend maximum possible questions irrespective of the exam analysis.</p>

<p>ATMA actually has three sections but this three sections are further divided into two as given below.</p>
