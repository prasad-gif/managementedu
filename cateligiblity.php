<h1>ELIGIBLITY CRITERIA FOR CAT EXAMINATION</h1>
<P>In order to give the CAT Exam, the candidates have to be eligible for it according to the CAT Exam Eligibility Criteria of 2021. CAT EXAM eligiblity is almost same every year. It doees not change every year. Below is the eligiblity criteria of CAT EXAMINATION.</P>
<ol>
  <li>In order to be eligible for the CAT Exam, the candidate has to pass at least their Bachelor's degree with at least 50% (for General Category and for candidates from the NC-OBC caste). For SC/ST and Differently Abled (DA) candidates, the percentage required is 45%.</li>
  <li>Candidates who have completed their professional degrees with over 50% (45% for SC/ST/PwD) are also eligible for CAT.</li>
  <li>If the University awards grades or CGPA instead of marks, the conversion factor given by the University has to be used. If there is no conversion factor specified by the University, the CGPA has to be converted into percentage by direct conversion i.e., divide the CGPA received by the highest CGPA possible and multiply the result by 100.</li>
<li>The percentage of marks obtained is calculated according to the practice of the University.</li>
<li>There is no age limit on the CAT Exam according to the CAT Exam Eligibility Criteria.</li>
<li>The candidate should have completed or be in his last year of bacholers degree which should be a three year degree or more. According to rules a two year degree is also considered unless the university is recognized by UGC. 
</ol> 


<h4>CAT Exam Eligibility for Final Year Students - </h4>
<p>Final year students are also eligible for taking CAT EXAMINATION. They are required to score 50% or more in aggregate in their passing year. If they are unable to fulfill this criteria their applications for colleges are considered void or in-eligible.</p>


<h4>Factors That Can Disqualify A Candidate - </h4>
<ol>
  <li>If the graduation is not recognized by the UGC/AICTE/MHRD, the candidate will be considered ineligible to appear for the CAT Exam.</li>
  <li>If the candidate has not appeared for their bachelor's degree and is in progress of completing their Professional Degree (CA/CS/ICWA)</li>
  <li>If the candidate is pursuing both their bachelor's degree as well as professional degree but has yet to complete both of them.</li>
</ol>


<h4>Eligiblity For Candidates With Disability - </h4>
<p>Candidates that have the following conditions are only taken into consideration</p>
<ol>
  <li>Candidates with locomotive disability or cerebral palsy</li>
  <li>Candidates having hearing impairment</li>
  <li>Candidates with low vision/blindness</li>
</ol>
<p>All this conditions are liable for candidates with disability. According to cat rules candidates must have atleast 40% of the disability mentioned above. Also this candidates are provided extra 20 minutes per section for their CAT EXAMINATION. candidates with problems related to vision and other locomotive disabilities are provided with a scribe and also a wheel chair according to the requirements.  </p>



<h4>Documents required and other eligiblity - </h4>
<p>Candidates dont need mandatory work experience. Thus large amount of people attend CAT EXAMINATION every year.</p>
<ol>
  <li>Post their admission in any of the B-Schools, the candidates will be required to furnish their final certificate from their graduation in order to continue in the institute.</li>
   <li>Since the candidate does not possess their final degree certificate with the total grade obtained, the candidate has to furnish an additional document with the signature of the Principal/Head of Department/ Registrar/ Director of the Institute verifying that the candidate has over 50% marks in the course currently.</li>
  </ol>