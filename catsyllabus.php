<h4>How To Go Through RC Passages</h4>
<p>The best way to solve RC passages is to read the full passage with active sense of mind. You can keep some keywords for every passage that can help you figure out what the particular passage is about. As it contain quality vocabulary as i said earlier you need to read and listen to new words. understanding the complete passage is the only key as CAT passages are not as they look. You can have a question that you think is related to upward passage but answer can be in the lower pasage. Focus and thoughtfull working of mind is important you should be able to relate to topics. The best way is to solve mock RC passages as many as you can.</p>
<ul>
  <li>The objective type of questions are again a little tricky to answer. These kinds of questions can be found mainly in analytical RC’s. Author, after expressing the pros and cons of both the sides mentioned in the passage, makes a neutral decision which he thinks is correct. Hence, an objective question holds an “object”, and has one specific answer which needs to strictly be of the language author used while writing the passage.</li>
  <li>The informative/scientific passages, the student needs to carefully read the facts mentioned in the passsages, as most of the questions will be based on it. If you are unable to understand some part of the passage that involves idiomatic expressions, you can leave it behind as reading it or trying to understand it hard would be a waste of time.</li>
  <li>The subjective kind of questions are easier as the aspirant needs to choose the answer option favoring the question. These kinds of questions can be mostly expected in persuasive RCs I.e RC passages where the author holds a pre-decided stand and explains reasons to justify why his stand is correct. So, the candidate can mark an answer option which is on the lines of the author’s argument, regardless of the fact that the language of the answer option matches with the passage.</li>
  <li>The critical and negative kind of passages usually have “distortions” as answer options. Candidates need to be aware that critical/sarcastic passages are mild forms of expression. Usually, the answer options can turn extremely bitter/acerbic/caustic and deviate from the central idea of the passage. Students need to ensure the answer option is polite in tone and still delivers some amount of sarcasm/criticism.</li>
</ul>



