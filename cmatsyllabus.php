<ul>
  <li>CMAT over all exam is not as hard as CAT exam. CMAT also has some easy question running to tough questions via moderate questions. </li>
  <li>CMAT general knowledge section is bit hard as variety of questions are asked in this section.</li>
  <li>Each section in CMAT has equal weightage that is 25%</li>
  <li>Candidates generally dont run out of time in CMAT exam because of its slightly easy pattern.</li>
  <li>CMAT can be a high score exam for candidates.</li>
  <li>Candidates should try to maximize their attempts with maximum possible accuracy. SOLVE AS MUCH AS YOU CAN!</li>
