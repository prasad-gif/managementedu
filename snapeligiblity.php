<h1>ELIGIBLITY CRITERIA FOR SNAP EXAMINATION AS PROVIDED BY OFFICIAL SNAP</h1>
<p>A candidate aspiring to join an MBA programme offered by any Institute of Symbiosis International (Deemed University) [SIU] has to take the common, mandatory Symbiosis National Aptitude (SNAP) Computer Based Test (CBT) 2020.</p>
<h4>To be eligible for SNAP examination candidates should folloe the following - </h4>
<p>
  <ol>
    <li>Register for SNAP Test,</li>
     <li>Pay the registration fee for SNAP Test,</li>
      <li>Register for her/his choice of programme(s) offered by any Institute of SIU,</li>
       <li>Pay the registration fee, separately for each programme.</li>
  </ol>
</p>
<p>If shortlisted for a programme applied to, the candidate would then need to participate in the further admission process (GE-PIWAT) of that particular programme.</p>

<p>The registration process (including the payment of the registration fee) for SNAP and Institutes is integrated. A candidate can complete the SNAP and programme registration (as well as pay the registration fee) on the same portal (snaptest.org).</p>

<P>SNAP is the main test to be eligible for any of the symbosis university or college. Different symbosis institute provide many different courses and degrees. Candidate can choose any of their intrest unless they fulfill the eligiblity criteria. Every Symbosis college have different eligiblity criterias for different courses. candidates are requested to go through all and then fill the application form.

  <p>
    <h4>Other eligiblity criterias as provided by official SNAP</h4>
  </p>
  <ol>
    <li>A candidate who has completed qualifying qualification from any Foreign University must obtain an equivalence certificate from Association of Indian Universities (AIU)</li>
     <li>Candidate should be a graduate from any recognised University/ Institution of National Importance and must have obtained a minimum of 50% marks or equivalent grade (45% marks or equivalent grade for Scheduled Castes/ Scheduled Tribes) at graduation.</li>
      <li>Candidates appearing for final year examinations can also apply, but their admission will be subject to obtaining a minimum of 50% marks or equivalent grade (45% or equivalent grade for Scheduled Castes/ Scheduled Tribes).</li>
</ol>

<h4>Reservation Policy for candidates as provided by SNAP.</h4>
<p> Following will be the reservation policy for admission to any programme (offered by any Institute) of SIU (except Symbiosis Institute of Management Studies, which has a separate policy#)</p>
<h4>Scheduled Castes Candidates - </h4>
<p>(15% of seats within the sanctioned intake) 
The University has provided for reservation to the extent of 15 % for all its academic programmes to Scheduled Castes Candidates, as defined under Article 341 of the Constitution.</p>
<h4>Scheduled Tribes Candidates - </h4>
<p>(7.5% of seats within the sanctioned intake)
The University has provided for reservation to the extent of 7.5 % for all its academic programmes to Scheduled Tribes Candidates, as defined under Article 342 of the Constitution.</p>
<h4>Differently Abled Candidates - </h4>
<p>(3% of seats within the sanctioned intake)
In compliance to the Government directives on Disability Guidelines issued by the Office of the Chief Commissioner for persons with disabilities vide Ministry of Social Justice and Empowerment, New Delhi notification no. 16-18/97 on 1st June 2001, The Rights of Persons with Disabilities Act, 2016 dated 27th December 2016 and University Grants Commission Notification No. F.6-1/2006 (CPP-II/SCT) dated 30/09/2009, the University has made provision of reserving 3% seats for Differently Abled Category (DAC) candidates in all its Constituents.</p>
<p>The types of disabilities covered are - </p>
<p>
  <ol>
    <li>Dyslexia</li>
     <li>Mental Retardation</li>
      <li>Hearing Impairment</li>
       <li>Multiple Disabilities</li>
        <li>Locomotors Disabilities</li>
         <li>Vision Impairment</li>
  </ol>
</p>
<h4>Children/ Wards of Defence personnel / Defence Special Category</h4>
<p>Children / Wards of Defence personnel means children/ wards of those Defence Personnel who are serving / retired Defence personnel from Army, Navy, Air Force. Retired Defence personnel are those who fall in the category of ex-servicemen as laid down in Ministry of Defence letter no. 36035/5/85- Est (SCT) dated 14 April 1987. It also includes children of Defence personnel, who died while in service or after retirement</p>

<p>The University in terms of the provisions of Memorandum of Association (MoA) between Symbiosis and Ministry of Defence, Govt. of India dated 10th July, 2000 provides for reservations up to the extent of 90% for Master of Business Administration programme of Symbiosis Institute of Management Studies (SIMS), comprising of 80 % for Children/ Wards of Defence personnel and 10 % for Special Defence Category. The University also provides 5 % reservation within the sanctioned intake for Children/ Wards of Defence Personnel in Constituents as listed below - </p>

<p>
  <ol>
    <li>Master of Business Administration (Information Technology) programme conducted by Symbiosis Institute of Computer Studies & Research (SICSR), Pune </li>
    <li>Master of Business Administration (Information Technology Business Management) programme conducted by Symbiosis Centre for Information Technology (SCIT), Pune </li>
    <li>Master of Business Administration (International Business) programme conducted by Symbiosis Institute of International Business (SIIB), Pune</li>
    <li>Master of Business Administration (Digital and Telecom Management) programme conducted by Symbiosis Institute of Digital & Telecom Management (SIDTM), Pune</li>
</ol>
</p>

