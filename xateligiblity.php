<h1>ELIGIBLITY CRITERIA FOR XAT EXAMINATION</h1>
<P>To be eligible for XAT examination candidates should read the norms as provided by XLRI jamshedpur. Following is the criteria that every candidate must meet.</P>
<ol>
  <li>Candidate must have atleast 3years of bacholers degree or equivalent</li>
  <li>Final year students can also apply if they are graduated before june 10 - 2020 criteria</li>
  <li>Foreign candidates can also apply</li>
  <li>XLRI do not impose minimum high school or graduation marks to students</li>
</ol>



<h4>Factors That Can Disqualify A Candidate - </h4>
<ol>
  <li>If the candidate has not appeared for their bachelor's degree and is in progress of completing their Professional Degree (CA/CS/ICWA)</li>
  <li>If the candidate is pursuing both their bachelor's degree as well as professional degree but has yet to complete both of them.</li>
</ol>


<h4>Eligiblity For Candidates With Disability - </h4>
<p>Candidates that have the following conditions are only taken into consideration</p>
<ol>
  <li>Candidates with locomotive disability or cerebral palsy</li>
  <li>Candidates having hearing impairment</li>
  <li>Candidates with low vision/blindness</li>
</ol>
<p>All this conditions are liable for candidates with disability. According to cat rules candidates must have atleast 40% of the disability mentioned above. Also this candidates are provided extra 25 minutes per section for their CAT EXAMINATION. candidates with problems related to vision and other locomotive disabilities are provided with a scribe and also a wheel chair according to the requirements.  </p>

