<p>Earlier, the XAT Paper Pattern included a question on essay writing but it has been removed from the XAT Exam Pattern since 2018. All the questions in the XAT Exam Pattern 2020 were MCQs with 5 options compared to the 4 options in CAT. However XAT Exam Pattern 2020 also included a new type of question called the Numerical Aptitude Type (NAT) in which the candidates have to input the answer using the virtual keyboard on the screen. This type of question can be expected to remain in XAT Exam 2021.
