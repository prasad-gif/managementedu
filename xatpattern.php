<h1>XAT EXAM PATTERN</h1>
<p>XAT Exam is one of the biggest MBA entrance exams which most MBA aspirants attempt along with CAT. Many top colleges accept XAT score for admissions. Same as CMAT the level of difficulty of XAT exam is increased by general knowledge section which is considered as hard. XAT is held in two parts. First part includes quantative aptitude, logical reasoning and dfata intrepretation and reading comprehension and second part consist of general knowledge. XAT exam is generally held in january. XAT was earlier a paper and pen exam but then it changed its pattern in 2018 and shifted it in a computer based exam.</p>
